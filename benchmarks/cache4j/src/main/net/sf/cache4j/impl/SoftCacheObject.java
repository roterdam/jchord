package net.sf.cache4j.impl;

import java.lang.ref.SoftReference;

/**
 * Класс SoftCacheObject это магкая :) оболочка для кешируемых объектов.
 * Для хранения ссылки на объект используется класс <code>java.lang.ref.SoftReference</code>
 *
 * @version $Revision: 1.0 $ $Date:$
 * @author Yuriy Stepovoy. <a href="mailto:stepovoy@gmail.com">stepovoy@gmail.com</a>
 **/

public class SoftCacheObject extends CacheObject {
// ----------------------------------------------------------------------------- Константы
// ----------------------------------------------------------------------------- Атрибуты класса
// ----------------------------------------------------------------------------- Статические переменные
// ----------------------------------------------------------------------------- Конструкторы

    /**
     * Конструктор
     * @param objId идентификатор кешируемого объекта
     */
    SoftCacheObject(Object objId) {
        super(objId);
    }

// ----------------------------------------------------------------------------- Public методы

    /**
     * Возвращает кешируемый объект
     */
    Object getObject() {
        return _obj==null ? null : ((SoftReference)_obj).get();
    }

    /**
     * Устанавливает кешируемый объект
     */
    void setObject(Object obj) {
        _obj = obj==null ? null : new SoftReference(obj);
    }

// ----------------------------------------------------------------------------- Package scope методы
// ----------------------------------------------------------------------------- Protected методы
// ----------------------------------------------------------------------------- Private методы
// ----------------------------------------------------------------------------- Inner классы
}

